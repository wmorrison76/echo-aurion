/**
 * Audit Event Contract for System-Wide Actions
 * 
 * Defines the standard audit events that must be emitted for critical actions.
 * EchoAI^3 consumes these events for auditing and learning.
 * 
 * Event Categories:
 * - BEO Lifecycle: creation, updates, approval, execution
 * - Order Lifecycle: placed, modified, received
 * - Production: scheduled, started, completed
 * - Schedule: generated, published, modified
 * - Staff: shortage forecast, job share posted/filled
 * - Menu: created, modified, published
 */

/** Standard audit event types for critical actions */
export const AUDIT_EVENT_TYPES = {
  // BEO Lifecycle
  BEO_CREATED: "BEO_CREATED",
  BEO_UPDATED: "BEO_UPDATED",
  BEO_APPROVED: "BEO_APPROVED",
  BEO_EXECUTED: "BEO_EXECUTED",
  BEO_CANCELLED: "BEO_CANCELLED",

  // Order Lifecycle
  ORDER_PLACED: "ORDER_PLACED",
  ORDER_MODIFIED: "ORDER_MODIFIED",
  ORDER_RECEIVED: "ORDER_RECEIVED",
  ORDER_CANCELLED: "ORDER_CANCELLED",

  // Production
  PRODUCTION_SCHEDULED: "PRODUCTION_SCHEDULED",
  PRODUCTION_STARTED: "PRODUCTION_STARTED",
  PRODUCTION_COMPLETED: "PRODUCTION_COMPLETED",

  // Schedule
  SCHEDULE_GENERATED: "SCHEDULE_GENERATED",
  SCHEDULE_PUBLISHED: "SCHEDULE_PUBLISHED",
  SCHEDULE_MODIFIED: "SCHEDULE_MODIFIED",

  // Staff
  STAFF_SHORTAGE_FORECAST: "STAFF_SHORTAGE_FORECAST",
  JOB_SHARE_POSTED: "JOB_SHARE_POSTED",
  JOB_SHARE_FILLED: "JOB_SHARE_FILLED",

  // Menu
  MENU_ITEM_ADDED: "MENU_ITEM_ADDED",
  MENU_ITEM_MODIFIED: "MENU_ITEM_MODIFIED",
  MENU_PUBLISHED: "MENU_PUBLISHED",

  // EchoAI^3 Specific
  ECHOAI_RECOMMENDATION: "ECHOAI_RECOMMENDATION",
  ECHOAI_LEARNING_CAPTURED: "ECHOAI_LEARNING_CAPTURED",
} as const;

export type AuditEventType = (typeof AUDIT_EVENT_TYPES)[keyof typeof AUDIT_EVENT_TYPES];

/** Base audit event structure */
export interface AuditEvent {
  eventId: string;
  eventType: AuditEventType;
  timestamp: string;
  tenantId: string;
  userId: string;
  entityType: string;
  entityId: string;
  beoNumber?: string; // For BEO traceability
  metadata: Record<string, unknown>;
  source: "system" | "user" | "echoai";
}

/** BEO-specific audit event */
export interface BEOAuditEvent extends AuditEvent {
  eventType:
    | typeof AUDIT_EVENT_TYPES.BEO_CREATED
    | typeof AUDIT_EVENT_TYPES.BEO_UPDATED
    | typeof AUDIT_EVENT_TYPES.BEO_APPROVED
    | typeof AUDIT_EVENT_TYPES.BEO_EXECUTED
    | typeof AUDIT_EVENT_TYPES.BEO_CANCELLED;
  entityType: "beo";
  metadata: {
    guestCount?: number;
    menuItems?: number;
    status?: string;
    changes?: string[];
    approvedBy?: string;
  };
}

/** Order-specific audit event */
export interface OrderAuditEvent extends AuditEvent {
  eventType:
    | typeof AUDIT_EVENT_TYPES.ORDER_PLACED
    | typeof AUDIT_EVENT_TYPES.ORDER_MODIFIED
    | typeof AUDIT_EVENT_TYPES.ORDER_RECEIVED
    | typeof AUDIT_EVENT_TYPES.ORDER_CANCELLED;
  entityType: "order";
  metadata: {
    lineItems?: number;
    totalValue?: number;
    supplier?: string;
    beoReference?: string;
  };
}

/** Production-specific audit event */
export interface ProductionAuditEvent extends AuditEvent {
  eventType:
    | typeof AUDIT_EVENT_TYPES.PRODUCTION_SCHEDULED
    | typeof AUDIT_EVENT_TYPES.PRODUCTION_STARTED
    | typeof AUDIT_EVENT_TYPES.PRODUCTION_COMPLETED;
  entityType: "production";
  metadata: {
    station?: string;
    taskCount?: number;
    assignedStaff?: number;
    beoReference?: string;
  };
}

/** Schedule-specific audit event */
export interface ScheduleAuditEvent extends AuditEvent {
  eventType:
    | typeof AUDIT_EVENT_TYPES.SCHEDULE_GENERATED
    | typeof AUDIT_EVENT_TYPES.SCHEDULE_PUBLISHED
    | typeof AUDIT_EVENT_TYPES.SCHEDULE_MODIFIED;
  entityType: "schedule";
  metadata: {
    staffCount?: number;
    totalHours?: number;
    coverageScore?: number;
    autoGenerated?: boolean;
  };
}

/** Staff shortage audit event */
export interface StaffShortageAuditEvent extends AuditEvent {
  eventType:
    | typeof AUDIT_EVENT_TYPES.STAFF_SHORTAGE_FORECAST
    | typeof AUDIT_EVENT_TYPES.JOB_SHARE_POSTED
    | typeof AUDIT_EVENT_TYPES.JOB_SHARE_FILLED;
  entityType: "staff";
  metadata: {
    shortageCount?: number;
    affectedRoles?: string[];
    jobShareOpportunities?: number;
    filledBy?: string;
  };
}

/** Menu-specific audit event */
export interface MenuAuditEvent extends AuditEvent {
  eventType:
    | typeof AUDIT_EVENT_TYPES.MENU_ITEM_ADDED
    | typeof AUDIT_EVENT_TYPES.MENU_ITEM_MODIFIED
    | typeof AUDIT_EVENT_TYPES.MENU_PUBLISHED;
  entityType: "menu";
  metadata: {
    itemName?: string;
    category?: string;
    price?: number;
    recipeId?: string;
  };
}

/** EchoAI^3 audit event */
export interface EchoAIAuditEvent extends AuditEvent {
  eventType:
    | typeof AUDIT_EVENT_TYPES.ECHOAI_RECOMMENDATION
    | typeof AUDIT_EVENT_TYPES.ECHOAI_LEARNING_CAPTURED;
  entityType: "echoai";
  source: "echoai";
  metadata: {
    recommendationType?: string;
    confidence?: number;
    accepted?: boolean;
    learningCategory?: string;
    dataPoints?: number;
  };
}

/** Union type for all audit events */
export type SystemAuditEvent =
  | BEOAuditEvent
  | OrderAuditEvent
  | ProductionAuditEvent
  | ScheduleAuditEvent
  | StaffShortageAuditEvent
  | MenuAuditEvent
  | EchoAIAuditEvent
  | AuditEvent;

/** Create a unique event ID */
export function createEventId(): string {
  return `evt-${Date.now()}-${Math.random().toString(36).slice(2, 8)}`;
}

/** Create base audit event */
export function createAuditEvent(
  eventType: AuditEventType,
  tenantId: string,
  userId: string,
  entityType: string,
  entityId: string,
  metadata: Record<string, unknown> = {},
  options: {
    beoNumber?: string;
    source?: "system" | "user" | "echoai";
  } = {},
): AuditEvent {
  return {
    eventId: createEventId(),
    eventType,
    timestamp: new Date().toISOString(),
    tenantId,
    userId,
    entityType,
    entityId,
    beoNumber: options.beoNumber,
    metadata,
    source: options.source || "system",
  };
}
