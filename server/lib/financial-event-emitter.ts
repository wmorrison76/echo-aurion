/**
 * Server-Side Financial Event Emitter
 * Manages event subscriptions and broadcasting for financial events
 * Server counterpart to the client-side FinancialEventBus
 * 
 * INTEGRATED WITH PERSISTENT EVENT STORE for durability
 */

import { EventEmitter } from "events";
import { logger } from "./logger";
import { PersistentEventStoreService } from "../services/persistent-event-store";

/**
 * Financial event types
 */
export type FinancialEventType =
  | "shift:created"
  | "shift:updated"
  | "shift:deleted"
  | "recipe:cost-updated"
  | "plate:sold"
  | "inventory:consumed"
  | "inventory:waste-logged"
  | "purchase:invoice-recorded"
  | "purchase:invoice-updated"
  | "event:cost-finalized"
  | "event:budget-updated"
  | "payroll:run-posted";

/**
 * Financial event payload
 */
export interface FinancialEventPayload {
  type: FinancialEventType;
  timestamp: number;
  outlet_id: string;
  org_id: string;
  data: Record<string, any>;
  metadata?: {
    source: string;
    user_id?: string;
    transaction_id?: string;
  };
}

/**
 * Server-side Financial Event Emitter (Singleton)
 * Now integrated with persistent event store for durability
 */
class FinancialEventEmitterImpl extends EventEmitter {
  private eventHistory: FinancialEventPayload[] = [];
  private maxHistorySize = 1000;
  private persistentStore?: PersistentEventStoreService;
  private persistEnabled: boolean = true;

  constructor() {
    super();
    this.setMaxListeners(100); // Allow up to 100 listeners
    // Initialize persistent store
    try {
      this.persistentStore = new PersistentEventStoreService();
      this.persistEnabled = true;
    } catch (error) {
      logger.warn("[FinancialEventEmitter] Failed to initialize persistent store, using in-memory only", {
        error: error instanceof Error ? error.message : String(error),
      });
      this.persistEnabled = false;
    }
  }

  /**
   * Emit a financial event
   * Now persists to event store (fire-and-forget) before emitting
   */
  emitFinancialEvent(event: FinancialEventPayload): void {
    // Persist to event store (fire-and-forget, don't block emission)
    if (this.persistEnabled && this.persistentStore) {
      // Generate idempotency key from event
      const idempotencyKey = event.metadata?.transaction_id || 
        `${event.type}:${event.outlet_id}:${event.org_id}:${event.timestamp}`;

      this.persistentStore.persistEvent({
        event_id: `fin-${Date.now()}-${Math.random().toString(36).substring(7)}`,
        tenant_id: event.org_id, // Using org_id as tenant_id
        org_id: event.org_id,
        event_type: event.type,
        source_bus: 'financial',
        priority: 'normal', // Could be enhanced to derive from event type
        payload: event.data,
        metadata: {
          ...event.metadata,
          outlet_id: event.outlet_id,
          original_timestamp: event.timestamp,
        },
        idempotency_key: idempotencyKey,
        event_hash: '', // Will be generated by persistEvent
        status: 'pending',
        retry_count: 0,
        max_retries: 3,
        schema_version: 1,
        occurred_at: new Date(event.timestamp).toISOString(),
      }).catch((persistError) => {
        // Log error but continue with in-memory emit (graceful degradation)
        logger.error("[FinancialEventEmitter] Failed to persist event, continuing with in-memory", {
          error: persistError instanceof Error ? persistError.message : String(persistError),
          event_type: event.type,
        });
      });
    }

    // Add to in-memory history (for backward compatibility)
    this.eventHistory.push(event);
    if (this.eventHistory.length > this.maxHistorySize) {
      this.eventHistory.shift();
    }

    // Emit to specific event type listeners
    this.emit(event.type, event);

    // Emit to wildcard 'all' listeners
    this.emit("all", event);

    logger.debug("[FinancialEventEmitter] Event emitted", {
      type: event.type,
      outlet_id: event.outlet_id,
      listeners: this.listenerCount(event.type),
      persisted: this.persistEnabled,
    });
  }

  /**
   * Subscribe to specific event type
   */
  onFinancialEvent(
    eventType: FinancialEventType,
    callback: (event: FinancialEventPayload) => void | Promise<void>,
  ): () => void {
    const handler = (event: FinancialEventPayload) => {
      try {
        const result = callback(event);
        if (result instanceof Promise) {
          result.catch((error) => {
            logger.error("[FinancialEventEmitter] Async handler error", {
              eventType,
              error: error instanceof Error ? error.message : String(error),
            });
          });
        }
      } catch (error) {
        logger.error("[FinancialEventEmitter] Handler error", {
          eventType,
          error: error instanceof Error ? error.message : String(error),
        });
      }
    };

    this.on(eventType, handler);

    // Return unsubscribe function
    return () => {
      this.off(eventType, handler);
    };
  }

  /**
   * Subscribe to all events
   */
  onAllEvents(callback: (event: FinancialEventPayload) => void): () => void {
    this.on("all", callback);
    return () => {
      this.off("all", callback);
    };
  }

  /**
   * Get event history
   */
  getHistory(
    type?: FinancialEventType,
    limit: number = 100,
  ): FinancialEventPayload[] {
    let events = this.eventHistory;

    if (type) {
      events = events.filter((e) => e.type === type);
    }

    return events.slice(-limit);
  }

  /**
   * Clear all listeners
   */
  clearAll(): void {
    this.removeAllListeners();
    this.eventHistory = [];
  }
}

// Export singleton instance
export const financialEventEmitter = new FinancialEventEmitterImpl();

/**
 * Helper to create event payload
 */
export function createFinancialEvent(
  type: FinancialEventType,
  outlet_id: string,
  org_id: string,
  data: Record<string, any>,
  metadata?: FinancialEventPayload["metadata"],
): FinancialEventPayload {
  return {
    type,
    timestamp: Date.now(),
    outlet_id,
    org_id,
    data,
    metadata,
  };
}

/**
 * Helper to emit shift:created event
 */
export function emitShiftCreatedEvent(
  outlet_id: string,
  org_id: string,
  shiftData: {
    shift_id: string;
    employee_id: string;
    start_time: number;
    end_time: number;
    hourly_rate: number;
    labor_cost: number;
  },
  userId?: string,
): void {
  const event = createFinancialEvent(
    "shift:created",
    outlet_id,
    org_id,
    shiftData,
    { source: "schedule-module", user_id: userId },
  );
  financialEventEmitter.emitFinancialEvent(event);
}

