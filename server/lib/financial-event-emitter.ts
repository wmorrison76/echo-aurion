/**
 * Helper to emit payroll:run-posted event
 */
export function emitPayrollRunPostedEvent(
  outlet_id: string,
  org_id: string,
  payrollData: {
    payroll_run_id: string;
    period_start: string; // YYYY-MM-DD
    period_end: string; // YYYY-MM-DD
    wages: number;
    taxes: number;
    benefits: number;
    deductions?: number;
    employee_count?: number;
    provider?: string;
  },
  userId?: string,
  eventTimestamp?: number,
): void {
  const event = createFinancialEvent(
    "payroll:run-posted",
    outlet_id,
    org_id,
    payrollData,
    {
      source: "payroll-module",
      user_id: userId,
      transaction_id: payrollData.payroll_run_id,
    },
  );

  if (typeof eventTimestamp === "number" && Number.isFinite(eventTimestamp)) {
    event.timestamp = eventTimestamp;
  }

  financialEventEmitter.emitFinancialEvent(event);
}
