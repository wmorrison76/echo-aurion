declare global {
  interface Window {
    html2canvas?: any;
    jspdf?: any;
  }
}
async function ensureLibs() {
  // Try globals first if (window.jspdf?.jsPDF && window.html2canvas) return { jsPDF: window.jspdf.jsPDF, html2canvas: window.html2canvas }; // Fallback to CDN UMDs await new Promise<void>((resolve, reject) => { const s1 = document.createElement("script"); s1.src ="https://cdn.jsdelivr.net/npm/jspdf@2.5.2/dist/jspdf.umd.min.js"; s1.onload = () => resolve(); s1.onerror = () => reject(new Error("Failed to load jsPDF")); document.head.appendChild(s1); }); await new Promise<void>((resolve, reject) => { const s2 = document.createElement("script"); s2.src ="https://cdn.jsdelivr.net/npm/html2canvas@1.4.1/dist/html2canvas.min.js"; s2.onload = () => resolve(); s2.onerror = () => reject(new Error("Failed to load html2canvas")); document.head.appendChild(s2); }); if (!window.jspdf?.jsPDF || !window.html2canvas) throw new Error("PDF libs unavailable"); return { jsPDF: window.jspdf.jsPDF, html2canvas: window.html2canvas };
}
export async function exportLayoutPDF(
  nodes: any[],
  metrics: any,
  warnings: string[],
) {
  try {
    const { jsPDF, html2canvas } = await ensureLibs();
    const pdf = new jsPDF({
      orientation: "landscape",
      unit: "pt",
      format: "a4",
    });
    const canvasEl = document.querySelector(
      "#planner-canvas",
    ) as HTMLElement | null;
    if (!canvasEl) throw new Error("Planner canvas not found.");
    const canvas = await html2canvas(canvasEl as HTMLElement, {
      backgroundColor: "#ffffff",
      scale: 2,
    });
    const imgData = canvas.toDataURL("image/png");
    const pageWidth = pdf.internal.pageSize.getWidth();
    const pageHeight = pdf.internal.pageSize.getHeight();
    const imgHeight = (canvas.height * pageWidth) / canvas.width;
    pdf.setFontSize(18);
    pdf.text("LUCCCA / EchoScope Layout Report", 40, 40);
    pdf.setFontSize(10);
    pdf.text(`Generated: ${new Date().toLocaleString()}`, 40, 55);
    pdf.text(`Nodes: ${nodes?.length ?? 0}`, 40, 67);
    pdf.addImage(
      imgData,
      "PNG",
      40,
      80,
      pageWidth - 80,
      imgHeight > 300 ? 300 : imgHeight,
    );
    let y = 400;
    pdf.setFontSize(12);
    pdf.text("EchoStratus Metrics", 40, y);
    y += 12;
    pdf.setFontSize(10);
    const entries: [string, any][] = [
      ["Seats Total", metrics?.seatsTotal],
      ["Seats / 1,000 sq ft", metrics?.seatsPerThousandSqFt],
      [
        "Projected Gross",
        metrics?.projectedGross != null
          ? `$${Number(metrics.projectedGross).toLocaleString()}`
          : "-",
      ],
      [
        "Labor Cost",
        metrics?.laborCost != null
          ? `$${Number(metrics.laborCost).toLocaleString()}`
          : "-",
      ],
      [
        "Revenue / Seat",
        metrics?.revenuePerSeat != null
          ? `$${Number(metrics.revenuePerSeat).toLocaleString()}`
          : "-",
      ],
      ["Density Rating", metrics?.densityRating],
    ];
    entries.forEach(([k, v]) => {
      pdf.text(`${k}: ${v ?? "-"}`, 60, y);
      y += 14;
    });
    y += 10;
    pdf.setFontSize(12);
    pdf.text("Warnings & Egress Notes", 40, y);
    y += 12;
    pdf.setFontSize(9);
    const wrap = (text: string, maxWidth: number) => {
      const words = String(text).split("");
      const out: string[] = [];
      let line = "";
      words.forEach((w) => {
        const test = line + w + "";
        if (pdf.getTextWidth(test) > maxWidth) {
          out.push(line);
          line = w + "";
        } else {
          line = test;
        }
      });
      out.push(line);
      return out;
    };
    for (const w of warnings || []) {
      const lines = wrap(w, pageWidth - 100);
      for (const ln of lines) {
        if (y > pageHeight - 40) {
          pdf.addPage();
          y = 40;
        }
        pdf.text(ln, 60, y);
        y += 12;
      }
    }
    pdf.setFontSize(8);
    pdf.text("Generated by LUCCCA EchoScope", 40, pageHeight - 20);
    pdf.text(
      `© ${new Date().getFullYear()} LUCCCA Systems`,
      pageWidth - 180,
      pageHeight - 20,
    );
    pdf.save(`EchoScope_Report_${Date.now()}.pdf`);
  } catch (err) {
    console.error("PDF Export failed:", err);
    alert("PDF export failed. Ensure jspdf and html2canvas are installed.");
  }
}
