import React from "react";

import { listBeosByEvent } from "@/lib/beo-store";
import { getGroup } from "@/lib/group-store";
import type { CalendarEvent } from "@/../shared/types/calendar";

type Props = {
  payload?: {
    groupId?: string;
    events?: CalendarEvent[];
  };
};

export default function GroupResumePrintView({ payload }: Props) {
  const groupId = payload?.groupId;
  const group = React.useMemo(
    () => (groupId ? getGroup(groupId) : null),
    [groupId],
  );
  const events = payload?.events ?? [];

  if (!groupId) return <div style={{ padding: 16 }}>No group selected.</div>;
  if (!group)
    return <div style={{ padding: 16 }}>Group not found: {groupId}</div>;

  return (
    <div
      style={{ padding: 18, fontFamily: "Arial, sans-serif", color: "#111" }}
    >
      <div style={{ textAlign: "center", marginBottom: 12 }}>
        <div style={{ fontSize: 22, fontWeight: 900 }}>Group Resume</div>
        <div style={{ fontSize: 16, fontWeight: 800, marginTop: 4 }}>
          {group.groupName}
        </div>
        <div style={{ opacity: 0.75, marginTop: 6, fontSize: 12 }}>
          {group.startDate ?? "—"} → {group.endDate ?? "—"}
        </div>
      </div>

      <div
        style={{
          border: "1px solid #222",
          borderRadius: 10,
          overflow: "hidden",
        }}
      >
        <div
          style={{
            display: "grid",
            gridTemplateColumns: "2fr 1.3fr 1.2fr 0.8fr 0.8fr 0.8fr 0.8fr",
            background: "#f3f3f3",
            fontWeight: 800,
            fontSize: 12,
            padding: 8,
          }}
        >
          <div>Event</div>
          <div>Date / Time</div>
          <div>Outlet / Room</div>
          <div>EXP</div>
          <div>GTD</div>
          <div>SET</div>
          <div>BEOs</div>
        </div>

        {events.length === 0 ? (
          <div style={{ padding: 10, fontSize: 12, opacity: 0.75 }}>
            No events provided to print view yet. (This is passed from
            `GroupResumePanel`.)
          </div>
        ) : (
          events.map((e) => {
            const beos = listBeosByEvent(e.id);
            return (
              <div
                key={e.id}
                style={{
                  display: "grid",
                  gridTemplateColumns:
                    "2fr 1.3fr 1.2fr 0.8fr 0.8fr 0.8fr 0.8fr",
                  fontSize: 12,
                  padding: 8,
                  borderTop: "1px solid rgba(0,0,0,0.12)",
                }}
              >
                <div style={{ fontWeight: 700 }}>{e.title}</div>
                <div>
                  {e.start} → {e.end}
                </div>
                <div>
                  <strong>{(e as any).locationName ?? "—"}</strong>
                  {(e as any).room ? ` / ${(e as any).room}` : ""}
                </div>
                <div>{(e as any).exp ?? "—"}</div>
                <div>{(e as any).gtd ?? "—"}</div>
                <div>{(e as any).set ?? "—"}</div>
                <div>{beos.length}</div>
              </div>
            );
          })
        )}
      </div>

      <div style={{ marginTop: 14, fontSize: 12, opacity: 0.7 }}>
        Generated by LUCCCA • EchoEventStudio • Group Resume (v1)
      </div>
    </div>
  );
}
