/**
 * Cake Layer Generation Queue
 * Manages the async generation of cake layers, frosting, and fillings
 * Queues requests, tracks progress, and manages regeneration
 */

import type { CakeLayer } from "@/shared/types";

export type LayerType = "tier" | "frosting" | "filling";

export interface LayerGenerationJob {
  id: string;
  type: LayerType;
  tierIndex: number;
  prompt: string;
  status: "pending" | "generating" | "completed" | "failed" | "approved";
  imageUrl?: string;
  error?: string;
  generatedAt?: string;
  approvedAt?: string;
}

export interface LayerGenerationQueue {
  designId: string;
  jobs: LayerGenerationJob[];
  totalTiers: number;
  createdAt: string;
  completedAt?: string;
}

export class CakeLayerQueueManager {
  private queue: LayerGenerationQueue;
  private onProgressCallback?: (progress: QueueProgress) => void;
  private onJobCompleteCallback?: (job: LayerGenerationJob) => void;

  constructor(designId: string, totalTiers: number) {
    this.queue = {
      designId,
      jobs: [],
      totalTiers,
      createdAt: new Date().toISOString(),
    };
  }

  /**
   * Add a job to the queue
   */
  addJob(
    type: LayerType,
    tierIndex: number,
    prompt: string,
  ): LayerGenerationJob {
    const job: LayerGenerationJob = {
      id: `${this.queue.designId}-${type}-${tierIndex}-${Date.now()}`,
      type,
      tierIndex,
      prompt,
      status: "pending",
      generatedAt: undefined,
    };

    this.queue.jobs.push(job);
    this.notifyProgress();
    return job;
  }

  /**
   * Add multiple jobs
   */
  addJobs(
    jobs: Array<{ type: LayerType; tierIndex: number; prompt: string }>,
  ): LayerGenerationJob[] {
    return jobs.map((job) => this.addJob(job.type, job.tierIndex, job.prompt));
  }

  /**
   * Get all jobs for a specific tier
   */
  getTierJobs(tierIndex: number): LayerGenerationJob[] {
    return this.queue.jobs.filter((job) => job.tierIndex === tierIndex);
  }

  /**
   * Get pending jobs
   */
  getPendingJobs(): LayerGenerationJob[] {
    return this.queue.jobs.filter((job) => job.status === "pending");
  }

  /**
   * Get next job to generate
   */
  getNextPendingJob(): LayerGenerationJob | undefined {
    return this.getPendingJobs()[0];
  }

  /**
   * Mark job as generating
   */
  setJobGenerating(jobId: string): void {
    const job = this.queue.jobs.find((j) => j.id === jobId);
    if (job) {
      job.status = "generating";
      this.notifyProgress();
    }
  }

  /**
   * Mark job as completed with image URL
   */
  completeJob(jobId: string, imageUrl: string): LayerGenerationJob | undefined {
    const job = this.queue.jobs.find((j) => j.id === jobId);
    if (job) {
      job.status = "completed";
      job.imageUrl = imageUrl;
      job.generatedAt = new Date().toISOString();
      this.onJobCompleteCallback?.(job);
      this.notifyProgress();
    }
    return job;
  }

  /**
   * Mark job as failed
   */
  failJob(jobId: string, error: string): LayerGenerationJob | undefined {
    const job = this.queue.jobs.find((j) => j.id === jobId);
    if (job) {
      job.status = "failed";
      job.error = error;
      this.notifyProgress();
    }
    return job;
  }

  /**
   * Approve a completed job
   */
  approveJob(jobId: string): LayerGenerationJob | undefined {
    const job = this.queue.jobs.find((j) => j.id === jobId);
    if (job && job.status === "completed") {
      job.status = "approved";
      job.approvedAt = new Date().toISOString();
      this.notifyProgress();
    }
    return job;
  }

  /**
   * Reject and regenerate a job
   */
  regenerateJob(jobId: string): LayerGenerationJob | undefined {
    const job = this.queue.jobs.find((j) => j.id === jobId);
    if (job) {
      // Create new job with same prompt
      const newJob = this.addJob(job.type, job.tierIndex, job.prompt);
      // Mark original as failed
      job.status = "failed";
      job.error = "Regenerated by user request";
      return newJob;
    }
    return undefined;
  }

  /**
   * Get queue progress
   */
  getProgress(): QueueProgress {
    const total = this.queue.jobs.length;
    const completed = this.queue.jobs.filter(
      (j) => j.status === "completed",
    ).length;
    const approved = this.queue.jobs.filter(
      (j) => j.status === "approved",
    ).length;
    const failed = this.queue.jobs.filter((j) => j.status === "failed").length;
    const generating = this.queue.jobs.filter(
      (j) => j.status === "generating",
    ).length;
    const pending = this.queue.jobs.filter(
      (j) => j.status === "pending",
    ).length;

    return {
      total,
      completed,
      approved,
      failed,
      generating,
      pending,
      percentComplete: total > 0 ? Math.round((completed / total) * 100) : 0,
      percentApproved: total > 0 ? Math.round((approved / total) * 100) : 0,
      isQueueDone: pending === 0 && generating === 0,
      allApproved: approved === total && total > 0,
    };
  }

  /**
   * Check if all jobs are approved
   */
  areAllApproved(): boolean {
    return (
      this.queue.jobs.length > 0 &&
      this.queue.jobs.every((job) => job.status === "approved")
    );
  }

  /**
   * Get all jobs grouped by type
   */
  getJobsByType(): Record<LayerType, LayerGenerationJob[]> {
    return {
      tier: this.queue.jobs.filter((j) => j.type === "tier"),
      frosting: this.queue.jobs.filter((j) => j.type === "frosting"),
      filling: this.queue.jobs.filter((j) => j.type === "filling"),
    };
  }

  /**
   * Export queue state
   */
  exportState(): LayerGenerationQueue {
    return JSON.parse(JSON.stringify(this.queue));
  }

  /**
   * Import queue state
   */
  importState(state: LayerGenerationQueue): void {
    this.queue = JSON.parse(JSON.stringify(state));
    this.notifyProgress();
  }

  /**
   * Register progress callback
   */
  onProgress(callback: (progress: QueueProgress) => void): void {
    this.onProgressCallback = callback;
  }

  /**
   * Register job complete callback
   */
  onJobComplete(callback: (job: LayerGenerationJob) => void): void {
    this.onJobCompleteCallback = callback;
  }

  /**
   * Notify progress
   */
  private notifyProgress(): void {
    this.onProgressCallback?.(this.getProgress());
  }

  /**
   * Get all jobs as CakeLayers for final composition
   */
  toApprovedLayers(): CakeLayer[] {
    const approvedJobs = this.queue.jobs.filter(
      (job) => job.status === "approved",
    );
    const layers: CakeLayer[] = [];

    // Group by tier
    for (let tierIndex = 0; tierIndex < this.queue.totalTiers; tierIndex++) {
      const tierJobs = approvedJobs.filter((j) => j.tierIndex === tierIndex);

      // Find tier, frosting, and filling for this tier
      const tierJob = tierJobs.find((j) => j.type === "tier");
      const frostingJob = tierJobs.find((j) => j.type === "frosting");
      const fillingJob = tierJobs.find((j) => j.type === "filling");

      if (tierJob?.imageUrl) {
        layers.push({
          id: `tier-${tierIndex}`,
          type: "tier",
          imageUrl: tierJob.imageUrl,
          generatedWith: "sdxl",
          prompt: tierJob.prompt,
          metadata: {
            generatedAt: tierJob.generatedAt || new Date().toISOString(),
            tierIndex,
            jobId: tierJob.id,
          },
        });
      }

      if (frostingJob?.imageUrl) {
        layers.push({
          id: `frosting-${tierIndex}`,
          type: "frosting",
          imageUrl: frostingJob.imageUrl,
          generatedWith: "sdxl",
          prompt: frostingJob.prompt,
          metadata: {
            generatedAt: frostingJob.generatedAt || new Date().toISOString(),
            tierIndex,
            jobId: frostingJob.id,
          },
        });
      }

      if (fillingJob?.imageUrl) {
        layers.push({
          id: `filling-${tierIndex}`,
          type: "filling",
          imageUrl: fillingJob.imageUrl,
          generatedWith: "sdxl",
          prompt: fillingJob.prompt,
          metadata: {
            generatedAt: fillingJob.generatedAt || new Date().toISOString(),
            tierIndex,
            jobId: fillingJob.id,
          },
        });
      }
    }

    return layers;
  }
}

export interface QueueProgress {
  total: number;
  completed: number;
  approved: number;
  failed: number;
  generating: number;
  pending: number;
  percentComplete: number;
  percentApproved: number;
  isQueueDone: boolean;
  allApproved: boolean;
}

/**
 * Persist queue to localStorage
 */
export function saveQueueToStorage(
  designId: string,
  queue: LayerGenerationQueue,
): void {
  localStorage.setItem(`cake_layer_queue_${designId}`, JSON.stringify(queue));
}

/**
 * Load queue from localStorage
 */
export function loadQueueFromStorage(
  designId: string,
): LayerGenerationQueue | null {
  const stored = localStorage.getItem(`cake_layer_queue_${designId}`);
  return stored ? JSON.parse(stored) : null;
}

/**
 * Clear queue from localStorage
 */
export function clearQueueFromStorage(designId: string): void {
  localStorage.removeItem(`cake_layer_queue_${designId}`);
}
