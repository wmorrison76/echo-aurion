import fs from "node:fs";
import path from "node:path";

type Found = { label: string; file: string };

function exists(p: string) {
  try { fs.accessSync(p); return true; } catch { return false; }
}

function read(p: string) {
  return fs.readFileSync(p, "utf8");
}

function write(p: string, content: string) {
  fs.mkdirSync(path.dirname(p), { recursive: true });
  fs.writeFileSync(p, content);
}

function findFirst(candidates: Found[]): Found | null {
  for (const c of candidates) if (exists(c.file)) return c;
  return null;
}

function replaceOnce(file: string, from: RegExp, to: string) {
  const src = read(file);
  const next = src.replace(from, to);
  if (next === src) return false;
  write(file, next);
  return true;
}

function ensureLine(file: string, line: string) {
  const src = read(file);
  if (src.includes(line)) return false;
  write(file, src.trimEnd() + "\n" + line + "\n");
  return true;
}

function ensureSnippet(file: string, marker: string, snippet: string) {
  const src = read(file);
  if (src.includes(marker)) return false;
  write(file, src + "\n" + snippet + "\n");
  return true;
}

function ensurePanelRegistryEntry(panelRegistryPath: string) {
  const src = read(panelRegistryPath);

  // If echocoder already exists, do nothing.
  if (src.includes(`id: "echocoder"`) || src.includes(`id: 'echocoder'`) || src.includes(`echocoder:`)) {
    console.log("✅ panel-registry already contains echocoder entry");
    return;
  }

  // Try to insert inside PANEL_REGISTRY object.
  // This is a safe best-effort insertion near the end of the object.
  const insertion = `
  echocoder: {
    id: "echocoder",
    title: "EchoCoder",
    devOnly: true,
    lazy: () => import("@/client/developer/EchoCoder/client/components/studio/EchoCoderPanel"),
    defaultSize: { w: 720, h: 520 },
  },
`;

  const did = replaceOnce(
    panelRegistryPath,
    /export\s+const\s+PANEL_REGISTRY[\s\S]*?=\s*\{\s*/m,
    (m) => m + insertion
  );

  if (did) {
    console.log("✅ Inserted echocoder entry into panel-registry");
  } else {
    console.warn("⚠️ Could not auto-insert echocoder entry. Add it manually to PANEL_REGISTRY.");
  }
}

function main() {
  const repoRoot = process.cwd();
  if (!exists(path.join(repoRoot, "package.json"))) {
    console.error("❌ Run this from the repo root (where package.json is).");
    process.exit(1);
  }

  console.log("🛠  EchoCoder repair starting…");
  console.log("📍 Repo:", repoRoot);

  // 1) Fix duplicate EchoCoderPanel: make legacy file re-export dev panel (no destructive deletes)
  const legacyPanel = path.join(repoRoot, "client/components/studio/EchoCoderPanel.tsx");
  const devPanel = path.join(repoRoot, "client/developer/EchoCoder/client/components/studio/EchoCoderPanel.tsx");

  if (exists(devPanel)) {
    if (exists(legacyPanel)) {
      const reexport = `// AUTO-GENERATED by scripts/echocoder/repair.ts
// Legacy path kept for backward compatibility.
// This ensures there is only ONE real EchoCoderPanel implementation.

export { default } from "@/client/developer/EchoCoder/client/components/studio/EchoCoderPanel";
`;
      write(legacyPanel, reexport);
      console.log("✅ Normalized legacy EchoCoderPanel.tsx to re-export dev panel");
    } else {
      console.log("ℹ️ No legacy panel found at client/components/studio/EchoCoderPanel.tsx (ok)");
    }
  } else {
    console.warn("⚠️ Dev EchoCoderPanel not found at:", devPanel);
    console.warn("   Skipping panel normalization. (Check your EchoCoder folder path.)");
  }

  // 2) Fix App.tsx import + mount for EchoCoderHotkey (this is why your event does nothing)
  const appCandidates: Found[] = [
    { label: "client/App.tsx", file: path.join(repoRoot, "client/App.tsx") },
    { label: "client/src/App.tsx", file: path.join(repoRoot, "client/src/App.tsx") },
    { label: "frontend/src/App.tsx", file: path.join(repoRoot, "frontend/src/App.tsx") },
  ];
  const app = findFirst(appCandidates);

  const hotkeyDev = path.join(repoRoot, "client/developer/EchoCoder/client/components/dev/EchoCoderHotkey.tsx");
  const hotkeyLegacy = path.join(repoRoot, "client/components/dev/EchoCoderHotkey.tsx");

  if (app) {
    const appPath = app.file;
    const appSrc = read(appPath);

    // Ensure we import the hotkey from the correct location
    let desiredImport = "";
    if (exists(hotkeyDev)) {
      desiredImport = `import EchoCoderHotkey from "@/client/developer/EchoCoder/client/components/dev/EchoCoderHotkey";`;
    } else if (exists(hotkeyLegacy)) {
      desiredImport = `import EchoCoderHotkey from "@/client/components/dev/EchoCoderHotkey";`;
    } else {
      console.warn("⚠️ EchoCoderHotkey file not found in dev or legacy path.");
      console.warn("   Create it or confirm its location. (Open-panel listener likely missing.)");
    }

    if (desiredImport) {
      // Remove any existing EchoCoderHotkey import, then add our desired one once.
      let next = appSrc
        .replace(/^\s*import\s+EchoCoderHotkey\s+from\s+["'][^"']+["'];\s*$/m, "")
        .trimEnd();

      if (!next.includes(desiredImport)) {
        // Insert near other imports (after last import line)
        const lines = next.split("\n");
        let insertAt = 0;
        for (let i = 0; i < lines.length; i++) {
          if (/^\s*import\s+/.test(lines[i])) insertAt = i + 1;
        }
        lines.splice(insertAt, 0, desiredImport);
        next = lines.join("\n");
      }

      // Ensure <EchoCoderHotkey /> is rendered somewhere inside <App/>
      // We’ll add it right after the opening return fragment if not present.
      if (!next.includes("<EchoCoderHotkey")) {
        next = next.replace(
          /return\s*\(\s*<>\s*/m,
          (m) => m + "\n      <EchoCoderHotkey />\n"
        );
      }

      write(appPath, next + "\n");
      console.log(`✅ Fixed EchoCoderHotkey import + mount in ${app.label}`);
    }
  } else {
    console.warn("⚠️ Could not find App.tsx in common locations. Skipping hotkey wiring.");
  }

  // 3) Ensure panel registry has echocoder entry
  const registryCandidates: Found[] = [
    { label: "client/lib/panel-registry.ts", file: path.join(repoRoot, "client/lib/panel-registry.ts") },
    { label: "client/src/lib/panel-registry.ts", file: path.join(repoRoot, "client/src/lib/panel-registry.ts") },
    { label: "client/lib/panel-registry.tsx", file: path.join(repoRoot, "client/lib/panel-registry.tsx") },
  ];
  const reg = findFirst(registryCandidates);
  if (reg) {
    ensurePanelRegistryEntry(reg.file);
  } else {
    console.warn("⚠️ Could not find panel-registry.ts. Add echocoder panel entry manually.");
  }

  // 4) Ensure .gitignore ignores uploads/runtime artifacts (keeps repo clean)
  const gitignore = path.join(repoRoot, ".gitignore");
  if (exists(gitignore)) {
    const added1 = ensureLine(gitignore, "client/developer/EchoCoder/uploads/");
    const added2 = ensureLine(gitignore, "client/developer/EchoCoder/**/.backups/");
    const added3 = ensureLine(gitignore, "client/developer/EchoCoder/**/.cache/");
    if (added1 || added2 || added3) console.log("✅ Updated .gitignore to ignore EchoCoder runtime artifacts");
    else console.log("✅ .gitignore already ignores EchoCoder runtime artifacts");
  } else {
    console.warn("⚠️ .gitignore not found at repo root?");
  }

  console.log("\n✅ Repair complete.");
  console.log("Next:");
  console.log("  pnpm install");
  console.log("  pnpm dev");
  console.log("  pnpm echocoder:unlock   (10–60 min window)");
  console.log("  then in browser console:");
  console.log('    window.dispatchEvent(new CustomEvent("open-panel",{detail:{id:"echocoder"}}));');
}

main();
