#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { createServer } from "vite";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, "..");
const clientRoot = path.join(projectRoot, "client");
const panelRegistryPath = path.join(clientRoot, "lib", "panel-registry.ts");
const reportPath = path.join(projectRoot, "docs", "PANEL_STATUS_REPORT.md");

const extractRegistryBlock = (content) => {
  const registryStart = content.indexOf("export const PANEL_REGISTRY");
  if (registryStart === -1) return null;
  const braceStart = content.indexOf("{", registryStart);
  if (braceStart === -1) return null;
  let depth = 0;
  for (let i = braceStart; i < content.length; i += 1) {
    const char = content[i];
    if (char === "{") depth += 1;
    if (char === "}") {
      depth -= 1;
      if (depth === 0) {
        return content.slice(braceStart + 1, i);
      }
    }
  }
  return null;
};

const extractRegistryEntries = () => {
  if (!fs.existsSync(panelRegistryPath)) {
    throw new Error("panel-registry.ts not found");
  }
  const content = fs.readFileSync(panelRegistryPath, "utf8");
  const registryBlock = extractRegistryBlock(content);
  if (!registryBlock) throw new Error("panel-registry.ts registry block not found");
  const entries = {};
  let currentKey = null;
  registryBlock.split("\n").forEach((line) => {
    const keyMatch = line.match(/^\s*(?:"([^"]+)"|([a-zA-Z0-9_]+))\s*:/);
    if (keyMatch) {
      currentKey = keyMatch[1] || keyMatch[2];
    }
    if (currentKey) {
      const importMatch = line.match(/import\(["']([^"']+)["']\)/);
      if (importMatch) {
        entries[currentKey] = importMatch[1];
        currentKey = null;
      }
    }
  });
  return entries;
};

const resolveModuleGroup = (importPath) => {
  if (importPath.startsWith("@/modules/")) {
    const rest = importPath.replace("@/modules/", "");
    return rest.split("/")[0] || "modules";
  }
  if (importPath.startsWith("@/components/")) return "components";
  if (importPath.startsWith("@/")) return "client";
  return "client";
};

const runAudit = async () => {
  const entries = extractRegistryEntries();
  const server = await createServer({
    root: projectRoot,
    logLevel: "error",
    appType: "custom",
    server: { middlewareMode: true, hmr: false, ws: false },
  });

  const results = [];
  for (const [panelKey, importPath] of Object.entries(entries)) {
    const isStub = importPath.includes("/_stubs/") || importPath.includes("\\_stubs\\");
    let status = "ok";
    let detail = "";
    if (isStub) {
      status = "stub";
      detail = "stubbed import path";
    } else {
      try {
        const resolved = await server.pluginContainer.resolveId(importPath, undefined, {
          ssr: true,
        });
        if (!resolved) {
          status = "error";
          detail = "unresolved import";
        } else if (resolved.id && !resolved.id.startsWith("\0")) {
          if (!fs.existsSync(resolved.id)) {
            status = "error";
            detail = `resolved path missing: ${resolved.id}`;
          }
        }
      } catch (error) {
        status = "error";
        detail = error instanceof Error ? error.message : String(error);
      }
    }
    results.push({
      panelKey,
      importPath,
      moduleGroup: resolveModuleGroup(importPath),
      status,
      detail,
    });
  }

  await server.close();
  return results;
};

const writeReport = (results) => {
  const groups = new Map();
  results.forEach((entry) => {
    if (!groups.has(entry.moduleGroup)) groups.set(entry.moduleGroup, []);
    groups.get(entry.moduleGroup).push(entry);
  });

  const lines = ["# Panel Status Report", "", "Generated by `scripts/audit-panels-runtime.mjs`.", ""];
  for (const [group, entries] of groups.entries()) {
    lines.push(`## ${group}`, "");
    lines.push("| Panel | Import | Status | Details |");
    lines.push("| --- | --- | --- | --- |");
    entries.forEach((entry) => {
      const status = entry.status.toUpperCase();
      const detail = entry.detail ? entry.detail.replace(/\|/g, "\\|") : "";
      lines.push(`| ${entry.panelKey} | \`${entry.importPath}\` | ${status} | ${detail} |`);
    });
    lines.push("");
  }

  fs.mkdirSync(path.dirname(reportPath), { recursive: true });
  fs.writeFileSync(reportPath, lines.join("\n"));
};

try {
  const results = await runAudit();
  writeReport(results);
  const failures = results.filter((entry) => entry.status === "error");
  const stubs = results.filter((entry) => entry.status === "stub");
  console.log(`Runtime panel audit complete. Errors: ${failures.length}, stubs: ${stubs.length}`);
  if (failures.length) {
    process.exit(1);
  }
} catch (error) {
  console.error("Runtime panel audit failed:", error);
  process.exit(1);
}
